# slicing list
my_list = ["p", "y", "t", "h", "o", "n", "i", "n", "d", "o"]

# get list from index 3 to 5 (h, o, n)
print(my_list[3:6])

# get list from index 4 to end (o, n, i, n, d, o)
print(my_list[4:])

# get list from last index 4 to start (p, y, t, h, o)
print(my_list[:5])

# get list from -1 to -4 (o, d, n, i)
print(my_list[-1:-5])