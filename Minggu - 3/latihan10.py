# format integer
print("{0} bila diubah ke biner menjadi {0:b}".format(12))

# format float
print("format eksponensial: {0:e}".format(1566.3456))

# format pembulatan
print("sepertiga sama dengan: {0:.3f}".format(1/2))

# meratakan string
print("|{:<10}|{:^10}|{:>10}|".format("beras", "gula", "garam"))

x = 12.3456789
print("nilai x = %3.2f" %x)
print("nilai x = %3.4f" %x)